public class Calculator {
    /**
     * This method is used to add two integers.
     * @param numberOne This is the first parameter to add method
     * @param numberTwo This is the second parameter to add method
     * @return int This returns sum of numberOne and numberTwo.
     */
    public int add(int numberOne, int numberTwo) {
        return numberOne + numberTwo;
    }
}